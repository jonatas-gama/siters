		function envia(){

			let form = new FormData();			
			
			form.append("nome", document.getElementById('nome').value);
			form.append("email", document.getElementById('email').value);
			form.append("assunto", document.getElementById('assunto').value);
			form.append("mensagem", document.getElementById('mensagem').value);
			document.getElementById("enviando").style.width = "100%";
			
			fetch("envia.php",{
				method: "POST",
				body: form
			}).then(function(response){
				if(response.ok){
					response.json().then(function(data){
						document.getElementById("form").reset();
						document.getElementById("enviando").style.width = "0%";
						document.getElementById("form").insertAdjacentHTML("beforeend", `<div class="alert alert-primary text-center" role="alert">
						${data.msg}</div>`);
					})				
				}else{
					console.log('Ocorreu algum erro de rede');
				}	
			}).catch(function(error){
				console.log(error.message);
			})
		}		
        </script>
